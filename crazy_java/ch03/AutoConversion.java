public class AutoConversion{
    public static void main(String[] args)
    {
        int a = 6;
        System.out.println("a is " + a);
        float f = a;
        System.out.println("f is " + f);
        byte b = 9;
        System.out.println("b is " + b);
        double d = b;
        System.out.println("d is " + d);
        //char c = b;
        //System.out.println("c is " + c);

    }
}
